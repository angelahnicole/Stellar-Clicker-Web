// ===========================================================================================================================
// SCROLL EVENT
// --------------------------------------------------------------------------------------------------------------------------
// Sets the navbar opacity to 1 when at the top and sets it to 0.3 when scrolling down.
// ===========================================================================================================================
$(document).on('scroll', function (e) 
{
    if($(document).scrollTop() === 0)
    {
        $('.navbar').css('opacity', 1.0);
    }
    else
    {
        $('.navbar').css('opacity', 0.2);
    }
    
});

// ===========================================================================================================================
// NAVBAR HOVER EVENT
// --------------------------------------------------------------------------------------------------------------------------
// Sets the navbar opacity to 1 when hovered over and sets it to 0.3 when hovered out (when it's not at the top)
// ===========================================================================================================================
$(document).ready(function()
{
   $('.navbar').hover
    (function() // hover in
    {
        $('.navbar').css('opacity', 1.0);
    },
    function() // hover out
    {
        if($(document).scrollTop() !== 0)
        {
            $('.navbar').css('opacity', 0.2);
        }
    }); 
   
});

